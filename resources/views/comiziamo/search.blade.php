  function get_list_users(){
    $("#append_users").empty();
    var search_user=$("#search_user").val();


    if (search_user=="") {
      $("#search_user").attr("placeholder","@lang('comiziamo/lang.search_comiziato')");
      $("#container_search").show();
      $("#container_search").html("@lang('comiziamo/lang.not_result')");
    }else{
      $.get("/get_list_users_comiziamo",{search_user:search_user},
        function(data){
          
          var res =jQuery.parseJSON(data);
          var src;

          if (res.length==0) {
            
            $("#container_search").show();
            $("#container_search").html("@lang('comiziamo/lang.not_result')");
          }else{
            $("#container_search").hide();
            for (var i = 0; i < res.length; i++) {

              if (res[i].img=="") {

                src="comiziamo_repo/default_img.png?refresh=<?php echo rand(1,999); ?>"

              }else{

                src="comiziamo_repo/img_user/"+res[i].img+"?refresh=<?php echo rand(1,999); ?>"

              }

              $("#append_users").append(
                '<div class="col-md-4 mb-3">'+
                '<div class="card">'+
                '<div class="d-flex flex-column align-items-center text-center hero hero_'+res[i].id_user+'" style=""><img src="'+src+'" alt="Admin" class="rounded-circle" width="150" height="150" style="border-style: inset;z-index: 99;" onclick="change_vis(\'user_'+res[i].id_user+'\')"></div>'+
                '<div class="card-body px-2 pb-2 pt-1">'+
                '<p class="mb-0">'+
                '<strong>'+
                '<a href="#" class="text-secondary">'+res[i].nickname+'</a>'+
                '</strong>'+
                '</p>'+
                '<div class="d-flex mb-3 justify-content-between">'+
                '<div>'+
                '<p class="mb-0 small"><b>Info @lang("comiziamo/lang.profile")</b></p>'+
                '<p class="mb-0 small"><b>@lang("comiziamo/lang.level"): </b> '+res[i].level_char+'</p>'+
                '<p class="mb-0 small"><b>@lang("comiziamo/lang.allies"): </b> '+res[i].follower+'</p>'+
                '</div>'+
                // '<div class="text-right">'+
                // '<p class="mb-0 small"><b>Info comizi</b></p>'+
                // '<p class="mb-0 small"><b>Comizi: </b> 2</p>'+
                // '<p class="mb-0 small"><b>Vittorie: </b> 3</p>'+
                // '</div>'+
                '</div>'+
                '<div class="d-flex justify-content-between">'+
                '<div class="col px-0">'+
                '<button id="user_search_'+res[i].id_user+'" class="btn btn-outline-secondary follow_button btn-block" onclick="follow('+res[i].id_user+',\'search\')">@lang("comiziamo/lang.allies")</button>'+
                '</div>'+
                '</div>'+
                '</div>'+
                '</div>'+
                '</div>');

              $('head').append('<style>.hero_'+res[i].id_user+':before{content: ""; background-image: url('+src+'); background-size: cover; position: absolute; top: 0px; right: 0px; bottom: 0px; left: 0px;-webkit-filter: blur(2px);}</style>');


            }
          }

          read_button_follow("search");
